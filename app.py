def cache_json(func):
    @wraps(func)
    def wrap(*args, **kwargs):
        key = f"rinkimai_json"
        value = redis.get(key)
        if value:
            return value.decode()

        value = func(*args, **kwargs)
        redis.setex(key, timedelta(seconds=10), value)
        return jsonify(value)

    return wrap


